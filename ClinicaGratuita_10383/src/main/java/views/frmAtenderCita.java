    private void cargarDatosIniciales() {

        if (idCita <= 0) {
            JOptionPane.showMessageDialog(this, "ID de cita inválido.");
            return;
        }

        // 1) Traer cita
        cita = citaCtrl.obtenerPorId(idCita);
        if (cita == null) {
            JOptionPane.showMessageDialog(this, "No se encontró la cita: " + idCita);
            return;
        }

        // (Opcional) Regla: solo atender si está "en curso"
        String estado = (cita.getEstado() == null) ? "" : cita.getEstado().trim().toLowerCase();
        if (!estado.equals("en curso")) {
            JOptionPane.showMessageDialog(this,
                    "Esta cita no está en curso.\nEstado actual: " + estado);
            // Si quieres obligar regresar:
            // if (principal != null) principal.mostrarMenu();
            // return;
        }

        // 2) Traer paciente y doctor usando Controllers
        paciente = pacienteCtrl.obtenerPorId(cita.getId_paciente());
        doctor = doctorCtrl.obtenerPorId(cita.getId_doctor());

        // 3) Pintar datos (solo lectura)
        txtNombrePaciente.setText(paciente != null ? paciente.getNombre() : "(No encontrado)");
        txtEdad.setText(paciente != null ? String.valueOf(paciente.getEdad()) : "");
        txtMotivoConsulta.setText(cita.getMotivo_Consulta() != null ? cita.getMotivo_Consulta() : "");

        // 4) Cargar combo de medicamentos
        cargarMedicamentosCombo();

        // 5) Limpia campos de captura
        txtTratamiento.setText("");
        txtDuracionTratamiento.setText("");
        txtIndicacionesMedicamentos.setText("");
        medsModel.setRowCount(0);
        cmbMedicamento.setSelectedIndex(-1);
    }

    private void cargarMedicamentosCombo() {
        DefaultComboBoxModel<Object> m = new DefaultComboBoxModel<>();
        for (model.Medicamento med : medicamentoCtrl.obtenerTodos()) {
            m.addElement(new ComboItem(med.getId_medicamento(), med.getNombre()));
        }
        cmbMedicamento.setModel(m);
        cmbMedicamento.setSelectedIndex(-1);
    }

    private int getSelectedMedicamentoId() {
        Object sel = cmbMedicamento.getSelectedItem();
        if (sel instanceof ComboItem it) {
            return it.id;
        }
        return -1;
    }
