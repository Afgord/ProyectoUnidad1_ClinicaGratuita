    private void btnFinalizarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarConsultaActionPerformed

        if (idCita <= 0) {
            return;
        }

        String descripcion = txtTratamiento.getText().trim();
        if (descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La descripción del tratamiento/diagnóstico es obligatoria.");
            return;
        }

        int duracion;
        try {
            duracion = Integer.parseInt(txtDuracionTratamiento.getText().trim());
            if (duracion <= 0) {
                throw new NumberFormatException();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Duración inválida. Debe ser un número mayor a 0.");
            return;
        }

        int op = JOptionPane.showConfirmDialog(
                this,
                "¿Finalizar la consulta?\nEsto marcará la cita como 'completada'.",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );
        if (op != JOptionPane.YES_OPTION) {
            return;
        }
        
        System.out.println("ATENDER -> idCita=" + idCita);
        // 1) Finaliza consulta (SP)
        boolean ok = tratamientoCtrl.finalizarConsulta(idCita, descripcion, duracion);
        if (!ok) {
            JOptionPane.showMessageDialog(this, tratamientoCtrl.getUltimo_mensaje());
            return;
        }

        int idTratamiento = tratamientoCtrl.getUltimo_id_tratamiento();

        System.out.println("id_tratamiento creado: " + idTratamiento);

        if (idTratamiento <= 0) {
            JOptionPane.showMessageDialog(this, "No se obtuvo el ID del tratamiento.");
            return;
        }

        System.out.println("Cantidad de medicamentos en tabla: " + medsModel.getRowCount());

        System.out.println("ID TRATAMIENTO (OUT): " + idTratamiento);
        System.out.println("Filas medsModel: " + medsModel.getRowCount());

        // 2) Insertar medicamentos asociados (si hay)
        for (int i = 0; i < medsModel.getRowCount(); i++) {
            Object col0 = medsModel.getValueAt(i, 0);
            Object col1 = medsModel.getValueAt(i, 1);
            Object col2 = medsModel.getValueAt(i, 2);

            System.out.println("Fila " + i + " -> col0=" + col0 + " col1=" + col1 + " col2=" + col2);

            int idMedicamento = Integer.parseInt(col0.toString());
            String indicaciones = col2.toString();

            boolean okMedicamento = tmCtrl.agregarMedicamento(idTratamiento, idMedicamento, indicaciones);
            if (!okMedicamento) {
                System.out.println("FALLÓ insertar: " + tmCtrl.getUltimo_mensaje());
                JOptionPane.showMessageDialog(this, tmCtrl.getUltimo_mensaje());
                return;
            }
        }

        JOptionPane.showMessageDialog(this, "Consulta finalizada correctamente.");

        op = JOptionPane.showConfirmDialog(
                this,
                "¿Desea atender otra cita registrada?",
                "Continuar",
                JOptionPane.YES_NO_OPTION
        );

        if (principal != null) {
            if (op == JOptionPane.YES_OPTION) {
                principal.mostrarSeleccionarCita(); // abre el diálogo para elegir otra cita
            } else {
                principal.mostrarMenu();            // regresa al menú principal
            }
        }
    }//GEN-LAST:event_btnFinalizarConsultaActionPerformed

