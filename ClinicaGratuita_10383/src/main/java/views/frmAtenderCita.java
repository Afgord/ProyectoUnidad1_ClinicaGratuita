    private void btnAgregarMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarMedicamentoActionPerformed
        int idMedicamento = getSelectedMedicamentoId();
        if (idMedicamento <= 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un medicamento.");
            return;
        }

        String indic = txtIndicacionesMedicamentos.getText().trim();
        if (indic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Escribe indicaciones.");
            return;
        }

        // Evitar duplicados
        for (int i = 0; i < medsModel.getRowCount(); i++) {
            int existente = Integer.parseInt(medsModel.getValueAt(i, 0).toString());
            if (existente == idMedicamento) {
                JOptionPane.showMessageDialog(this, "Ese medicamento ya está agregado.");
                return;
            }
        }

        String nombre = cmbMedicamento.getSelectedItem().toString();
        medsModel.addRow(new Object[]{idMedicamento, nombre, indic});

        // limpiar indicaciones para el siguiente
        txtIndicacionesMedicamentos.setText("");
        cmbMedicamento.setSelectedIndex(-1);
    }//GEN-LAST:event_btnAgregarMedicamentoActionPerformed

    private void btnQuitarMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarMedicamentoActionPerformed
        // TODO add your handling code here:
        int row = tblMedicamentos.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un medicamento de la tabla.");
            return;
        }

        int op = JOptionPane.showConfirmDialog(
                this,
                "¿Quitar el medicamento seleccionado?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (op == JOptionPane.YES_OPTION) {
            medsModel.removeRow(row);
        }

    }//GEN-LAST:event_btnQuitarMedicamentoActionPerformed

