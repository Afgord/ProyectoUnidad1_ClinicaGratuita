    private void cargarPacientes() {
        DefaultComboBoxModel<Object> m = new DefaultComboBoxModel<>();

        for (Paciente p : pacienteCtrl.obtenerTodos()) {
            m.addElement(new ComboItem(p.getId_paciente(), p.getId_paciente() + " - " + p.getNombre()));
        }

        cmbPaciente.setModel(m);
        cmbPaciente.setSelectedIndex(-1);
    }

    private int getSelectedPacienteId() {
        Object sel = cmbPaciente.getSelectedItem();
        if (sel instanceof ComboItem it) {
            return it.id;
        }
        return -1;
    }

    private void limpiarDetalle() {
        txtNombre.setText("");
        txtEdad.setText("");
        txtSexo.setText("");
        txtDireccion.setText("");
        txtEmail.setText("");
        txtTelefono.setText("");

    }

    private void cargarDetallePaciente(int idPaciente) {
        Paciente p = pacienteCtrl.obtenerPorId(idPaciente);
        if (p == null) {
            JOptionPane.showMessageDialog(this, "No se encontró el paciente.");
            limpiarDetalle();
            return;
        }

        txtNombre.setText(p.getNombre());
        txtEdad.setText(String.valueOf(p.getEdad()));
        txtSexo.setText(p.getSexo());
        txtDireccion.setText(p.getDireccion());
        txtEmail.setText(p.getEmail());
        txtTelefono.setText(p.getTelefono());

    }

