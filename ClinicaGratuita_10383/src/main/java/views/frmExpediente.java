    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        int id = getSelectedPacienteId();
        if (id <= 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un paciente.");
            return;
        }

        Paciente p = pacienteCtrl.obtenerPorId(id);
        if (p == null) {
            JOptionPane.showMessageDialog(this, "No se encontró el paciente.");
            return;
        }

        p.setDireccion(txtDireccion.getText());
        p.setTelefono(txtTelefono.getText());
        p.setEmail(txtEmail.getText());

        int op = JOptionPane.showConfirmDialog(
                this,
                "¿Guardar cambios del paciente " + p.getNombre() + "?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );
        if (op != JOptionPane.YES_OPTION) {
            return;
        }

        boolean ok = pacienteCtrl.actualizar(p);
        JOptionPane.showMessageDialog(this, pacienteCtrl.getUltimo_mensaje());

        if (ok) {
            cargarDetallePaciente(id);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void cmbPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPacienteActionPerformed
        // TODO add your handling code here:
        int id = getSelectedPacienteId();
        if (id <= 0) {
            limpiarDetalle();
            txtId.setText("");
            return;
        }

        txtId.setText(String.valueOf(id));
        cargarDetallePaciente(id);
    }//GEN-LAST:event_cmbPacienteActionPerformed

